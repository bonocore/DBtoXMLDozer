package com.rh.demo;

import java.util.Date;

public class GasStorageData {

	private String acerCode;
	private Date startGas;
	private Date endGas;
	public String getAcerCode() {
		return acerCode;
	}
	public void setAcerCode(String acerCode) {
		this.acerCode = acerCode;
	}
	public Date getStartGas() {
		return startGas;
	}
	public void setStartGas(Date startGas) {
		this.startGas = startGas;
	}
	public Date getEndGas() {
		return endGas;
	}
	public void setEndGas(Date endGas) {
		this.endGas = endGas;
	}
	
	
	
}
