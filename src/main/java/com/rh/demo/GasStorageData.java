package com.rh.demo;

import java.util.Date;

public class GasStorageData {

	private String idCode;
	private Date startGas;
	private Date endGas;
	
	public String getIdCode() {
		return idCode;
	}
	public void setIdCode(String idCode) {
		this.idCode = idCode;
	}
	public Date getStartGas() {
		return startGas;
	}
	public void setStartGas(Date startGas) {
		this.startGas = startGas;
	}
	public Date getEndGas() {
		return endGas;
	}
	public void setEndGas(Date endGas) {
		this.endGas = endGas;
	}
	
	
	
}
